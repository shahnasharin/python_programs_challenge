#------------------------------------------sum of elements---------------------------


numbers = [1, 2, 3, 4, 5]
array_sum = 0
for num in numbers:
    array_sum += num

print("Sum of array elements:", array_sum)

#------------------------------------------------------OR---------------------------------------------

numbers = [1, 2, 3, 4, 5]
array_sum = sum(numbers)
print("Sum of array elements:", array_sum)

